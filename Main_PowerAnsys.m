    handles.PAR.Pathname=DataPathName;%字符串
    handles.PAR.Filename=DataFileName;%元胞数组{}
    handles.PAR.Filepath=DataFilePath;%全路径:路径+文件名{}
    set(handles.edit2,'String',DataPathName);
    set(handles.listbox1,'String',DataFileName);
    handles.Files=[];
    if ischar(DataFilePath)
        [data,text] = ReadDataFile(DataFilePath,2);% 1:读取动态理论曲线数据，2:读取功率曲线运行数据
        handles.Files(1).data=data;
        handles.Files(1).text=text;
        handles.Files(1).Filename=DataFileName;
    else
        for i=1:size(DataFilePath,2)
            [data,text] = ReadDataFile(DataFilePath{i},2);% 1:读取动态理论曲线数据，2:读取功率曲线运行数据
            handles.Files(i).data=data;
            handles.Files(i).text=text;%元胞数组1xN{}
            handles.Files(i).Filename=DataFileName{1,i};
        end
    end
    set(handles.popupmenu1,'String',text,'Value',1);
    set(handles.popupmenu2,'String',text,'Value',1);
    set(handles.popupmenu3,'String',text,'Value',1);
    set(handles.popupmenu4,'String',text,'Value',1);
    set(handles.popupmenu5,'String',text,'Value',1);
    set(handles.popupmenu6,'String',text,'Value',1);
end
